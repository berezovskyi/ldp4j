package org.ldp4j.server.api;

import java.net.URI;

import javax.ws.rs.core.HttpHeaders;
import javax.ws.rs.core.Request;
import javax.ws.rs.core.Response;
import javax.ws.rs.core.UriInfo;

public interface Endpoint {

	URI path();

	Response getHeader(UriInfo uriInfo, HttpHeaders headers, Request request);

	Response getOptions(UriInfo uriInfo, HttpHeaders headers, Request request);
	
	Response retrieveResource(UriInfo uriInfo, HttpHeaders headers, Request request);

	Response updateResource(UriInfo uriInfo, HttpHeaders headers, Request request, String entity);

	Response createResource(UriInfo uriInfo, HttpHeaders headers, Request request, String entity);

	Response patchResource(UriInfo uriInfo, HttpHeaders headers, Request request, String entity);

	Response deleteResource(UriInfo uriInfo, HttpHeaders headers, Request request);

}